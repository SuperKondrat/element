// Общая проверка формата почты (текст@домен.зона, зона от 2 символов) —
// сознательно не привязана к конкретному списку зон (.ru/.com/...): такой
// список пришлось бы бесконечно пополнять (.io, .dev, .info...), а общая
// проверка ловит те же опечатки без ограничения реальных доменов.
const EMAIL_PATTERN = /^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/;

export function isValidEmail(value: string): boolean {
  return EMAIL_PATTERN.test(value);
}
